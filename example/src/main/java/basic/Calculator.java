public class Calculator {
    int sub(int one, int two) {
        return one - two;
    }

    int add(int one, int two) {
        return one + two;
    }

    int mul(int one, int two) {
        return one * two;
    }

    int div(int one, int two) {
        return one / two;
    }

    int rem(int one, int two) {
        return one % two;
    }
}
